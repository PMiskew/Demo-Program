
public class Constants {

	public static void main(String[] args) {

		
		/*
		 * Constants are values that once set cannot be changed during the life of the program. 
		 * To make a variable constant we use the modifier final. 
		 */
		
		
		//By putt the modifier word in front of the type this variable is fixed forever and channot be changed. 
		final int NUMBER = 9;
		
		/*
		 * NOTE ON FORM:
		 * 
		 * Constants are always capitalized as this allows easy recognition.  The computer will accept lower case names, but 
		 * this is not advised.  
		 */
		
		
	}

}
